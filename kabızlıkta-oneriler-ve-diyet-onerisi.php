                                    NASIL YAPILIR?</h3>
                                <p>Yukarıda bahsedilen yaşam tarzı önlemlerinin uygulanması tedavi sürecinde büyük önem
                                    taşır.
                                </p>
                                <p>Özellikle yemek sonrası tuvalete oturma önerilmelidir. Kabızlık olgularında,
                                    doktorunuz
                                    çocuğa zarar vermeyecek ilaçları seçerek uygun dozda ve uzun süreli tedavi
                                    önerecektir.
                                    Doktorunuzun tavsiye ettiği ilaçları kullanırken bu rahatsızlığın çok sık görüldüğü,
                                    hastayı
                                    ve anne babaları çok yoran bir hastalık olduğu unutulmamalıdır. Bu açıdan doktor
                                    önerilerine
                                    harfiyen uyulması çok önem taşır. Verilen ilaç tedavisinin doktor tarafından tarif
                                    edilen
                                    biçimde en az 3-6ay&nbsp; süre ile kullanılması gerektiği bilinmelidir. En sık
                                    yapılan
                                    hatalar; ilaç tedavisinin uygun dozda verilmemesi veya yarar gören çocuklarda
                                    ilaçların kısa
                                    zaman sonra kesilmesidir. Tedavinin uzun süreli olması çocuğun ağrılı dışkılama
                                    deneyimini
                                    unutması açısından önemlidir.</p>
