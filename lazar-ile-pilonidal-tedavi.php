                            Cinsel organın ucundan karşıya işeyebilen bir çocuk (cinsel olarak aktif çağa gelindiğinde
                            sperm de ileriye
                            atılabilecek),
                        </li>
                    </ul>
                    <p class="my-1">Normale en yakın bir dış görünüm.</p>
                    <p>
                        Ameliyatlara başlandığında ilk iş olarak deri soyulduktan sonra organın içine serum verilerek “
                        suni sertleşme “
                        oluşturulur. Böylece eğriliğin derecesi saptanarak düzeltme yapılır.
                        <br> <br>
                        İkinci sırada, eksik olan idrar borusunun organın baş kısmının ucuna kadar taşınacak şekilde
                        tamamlanması vardır.
                        Kusurlu delik uca ne kadar uzaksa bu işlem o kadar zorlu olur. Büyüten gözlükler, saç kılı kadar
                        ince dikiş malzemesi
                        kullanılır. Her iş böyle olsa da özellikle bu ameliyatlarda cerrahi beceri ve deneyim çok öne
                        çıkar. İdrar borusunu
                        uzatırken veya oluşturduktan sonra örtmek ve dikiş hattını sağlamlaştırmak için “sünnet derisi “
                        kullanılır. Bu nedenle
                        bu çocukların önceden sünnet edilmemiş olması hayati bir konudur. Zaten ameliyat bittiğinde
                        hasta sünnet olmuş
                        olacaktır.
                        <br> <br>
                        Ameliyatın bir sonraki aşamasında organın baş kısmı içinden yeni boru geçecek şekilde kapatılır
                        ve idrar deliği uca
                        taşınmış olur. En sonunda cilt kapatılır ve ameliyata son verilir.
                        <br><br>
                        Yeni yapılan idrar borusu ve deliğinin içine bir sonda (tüp) konulur ve ilk 7-10 gün boyunca
                        idrarın bunun içinden
                        akması sağlanır. Basınçla işeme esnasında henüz taze dikişlerin zarar görmesinden kaçınmak için
                        bu yapılır. İşlemin
                        sonunda aşırı şişme ve kanamayı önleyici basınçlı bir pansuman materyeli organa sarılır.
                        <br><br>
                        Ameliyat sonrası iki hafta kadar hızlı bir iyileşme dönemi sürer, ilk haftanın sonunda pansuman
                        açılır ve genellikle
                        sonda çıkarılır. On – onbeş gün sonrasından itibaren hasta normal aktivitelerine dönebilir.

