                    <h2 class="my-2">HİPOSPADİAS NEDİR ?</h2>
                    <h2 class="my-2 text-uppercase">Hipospadyas (Halk dilinde peygamber sünneti veya yarım sünnet )
                        nedir ?</h2>
                    <p class="fs-md text-title mb-4 pb-2">
                        Normalde erkek çocuklarının cinsel organı çepeçevre bir deri ile örtülüdür. Bazı erkek
                        çocuklarında bu derinin organın
                        alt kısmında oluşmadığı, adeta horoz ibiği gibi üst tarafta yer aldığı gözlenir. Sünnet
                        derisinin bu hali dolayısıyla
                        halk arasında bu duruma “yarım sünnet” denir. “Peygamber sünneti” diyenler de vardır ve sözcüğün
                        saygın anlamı nedeniyle
                        aileler bu durumu iyi bir şey zannederler.
                        <br><br>
                        Oysa bu çocuklarda tek sorun sünnet derisinin bu değişik yapısı değildir. Asıl sorun, her
                        çocukta biraz farklı
                        derecelerde olsa bile, idrar deliğinin organın en ucunda olması gerekirken organın alt yüzünde
                        bir yerde olması, hatta
                        çok ağır kusurlarda torbaya veya makatın önünde bir yere açılmasıdır. Bu çocukların cinsel
                        organlarında, sertleşme
                        esnasında daha çok fark edilen bir eğrilik de olur. En ağır anomalilerde dış görünüm bir kız
                        çocuğunu andırır.
                        <br><br>
                        Hatalı delik cinsel organın uç kısmına ne kadar uzaksa anormallik de o kadar ağır demektir. Bazı
                        anne ve babalar ise
                        eğriliği en önce fark ederler.
                    </p>
                    <h4 class="my-2">Hipospadyas tedavisinin amaçları nelerdir?</h4>
                    <h5>Cerrahi tedavideki ana hedefler üç tanedir :</h5>
