                                        gece alt ıslatması olmayıp sonradan dönem dönem kaçırmaları olan hastalardır. Ayrıca
                                        enürezis nokturna hastaları eşlik eden gündüz işeme problemlerinin varlığına göre de
                                        sınıflandırılabilir. Bu sınıflamanın net olarak yapılması hastaya yaklaşımı belirlemede
                                        oldukça önemlidir. Bekli de bu gece kaçırmaları daha önemli bir böbrek veya mesane sorununun
                                        dışa yansıması olabilir.</p>
