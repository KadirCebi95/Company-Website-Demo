                                        Oldukça sık görülen gece ıslatmaları normal gelişim sürecinin bir parçası olarak kabul
                                        edilse de 5 yaşından sonra devam etmesi halinde çocuklarda psikolojik ve sosyolojik
                                        bozukluklara yol açtığı için klinik bir sorun olarak tanımlanabilir. Yaş ilerledikçe görülme
                                        sıklığı azalsa da (geçen her yılda %15 oranında azalma) 18 yaşındaki bireylerde %1 oranında
                                        sebat edebilir. Erkek çocuklarında daha sık izlenir. Anne- babada benzer hikayesi olan
                                        çocuklarda daha sık izlenmesi genetik bir yatkınlığının da söz konusu olduğunu
                                        göstermektedir.</p>
                                  
